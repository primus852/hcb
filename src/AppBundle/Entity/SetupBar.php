<?php

namespace AppBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity
 * @ORM\Table(name="app_setup_bar")
 */
class SetupBar
{
    /**
     * @ORM\Id
     * @ORM\Column(type="integer")
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    protected $id;

    /**
     * @ORM\Column(type="string", length=10)
     */
    protected $modSingle;

    /**
     * @ORM\Column(type="decimal", scale=2)
     */
    protected $valSingle;

    /**
     * @ORM\Column(type="string", length=10)
     */
    protected $modDouble;

    /**
     * @ORM\Column(type="decimal", scale=2)
     */
    protected $valDouble;

    /**
     * @ORM\Column(type="string", length=10)
     */
    protected $modTriple;

    /**
     * @ORM\Column(type="decimal", scale=2)
     */
    protected $valTriple;

    /**
     * @ORM\Column(type="string", length=10)
     */
    protected $modQuadruple;

    /**
     * @ORM\Column(type="decimal", scale=2)
     */
    protected $valQuadruple;

    /**
     * @ORM\Column(type="decimal", scale=2)
     */
    protected $valExtra;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set modSingle
     *
     * @param string $modSingle
     *
     * @return SetupBar
     */
    public function setModSingle($modSingle)
    {
        $this->modSingle = $modSingle;

        return $this;
    }

    /**
     * Get modSingle
     *
     * @return string
     */
    public function getModSingle()
    {
        return $this->modSingle;
    }

    /**
     * Set valSingle
     *
     * @param string $valSingle
     *
     * @return SetupBar
     */
    public function setValSingle($valSingle)
    {
        $this->valSingle = $valSingle;

        return $this;
    }

    /**
     * Get valSingle
     *
     * @return string
     */
    public function getValSingle()
    {
        return $this->valSingle;
    }

    /**
     * Set modDouble
     *
     * @param string $modDouble
     *
     * @return SetupBar
     */
    public function setModDouble($modDouble)
    {
        $this->modDouble = $modDouble;

        return $this;
    }

    /**
     * Get modDouble
     *
     * @return string
     */
    public function getModDouble()
    {
        return $this->modDouble;
    }

    /**
     * Set valDouble
     *
     * @param string $valDouble
     *
     * @return SetupBar
     */
    public function setValDouble($valDouble)
    {
        $this->valDouble = $valDouble;

        return $this;
    }

    /**
     * Get valDouble
     *
     * @return string
     */
    public function getValDouble()
    {
        return $this->valDouble;
    }

    /**
     * Set modTriple
     *
     * @param string $modTriple
     *
     * @return SetupBar
     */
    public function setModTriple($modTriple)
    {
        $this->modTriple = $modTriple;

        return $this;
    }

    /**
     * Get modTriple
     *
     * @return string
     */
    public function getModTriple()
    {
        return $this->modTriple;
    }

    /**
     * Set valTriple
     *
     * @param string $valTriple
     *
     * @return SetupBar
     */
    public function setValTriple($valTriple)
    {
        $this->valTriple = $valTriple;

        return $this;
    }

    /**
     * Get valTriple
     *
     * @return string
     */
    public function getValTriple()
    {
        return $this->valTriple;
    }

    /**
     * Set modQuadruple
     *
     * @param string $modQuadruple
     *
     * @return SetupBar
     */
    public function setModQuadruple($modQuadruple)
    {
        $this->modQuadruple = $modQuadruple;

        return $this;
    }

    /**
     * Get modQuadruple
     *
     * @return string
     */
    public function getModQuadruple()
    {
        return $this->modQuadruple;
    }

    /**
     * Set valQuadruple
     *
     * @param string $valQuadruple
     *
     * @return SetupBar
     */
    public function setValQuadruple($valQuadruple)
    {
        $this->valQuadruple = $valQuadruple;

        return $this;
    }

    /**
     * Get valQuadruple
     *
     * @return string
     */
    public function getValQuadruple()
    {
        return $this->valQuadruple;
    }

    /**
     * Set valExtra
     *
     * @param string $valExtra
     *
     * @return SetupBar
     */
    public function setValExtra($valExtra)
    {
        $this->valExtra = $valExtra;

        return $this;
    }

    /**
     * Get valExtra
     *
     * @return string
     */
    public function getValExtra()
    {
        return $this->valExtra;
    }
}
